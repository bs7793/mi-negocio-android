    SELECT 1 FROM pg_trigger WHERE tgname = 'update_product_option_values_updated_at'
  ) THEN
    CREATE TRIGGER update_product_option_values_updated_at
    BEFORE UPDATE ON public.product_option_values
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_trigger WHERE tgname = 'update_product_variants_updated_at'
  ) THEN
    CREATE TRIGGER update_product_variants_updated_at
    BEFORE UPDATE ON public.product_variants
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_trigger WHERE tgname = 'update_variant_inventory_updated_at'
  ) THEN
    CREATE TRIGGER update_variant_inventory_updated_at
    BEFORE UPDATE ON public.variant_inventory
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();
  END IF;

  IF NOT EXISTS (
    SELECT 1 FROM pg_trigger WHERE tgname = 'update_variant_inventory_movements_updated_at'
  ) THEN
    CREATE TRIGGER update_variant_inventory_movements_updated_at
    BEFORE UPDATE ON public.variant_inventory_movements
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();
  END IF;
END $$;

